/**
  * @brief This function handles TIM3 global interrupt.
  */
void TIM3_IRQHandler(void)
{
  /* USER CODE BEGIN TIM3_IRQn 0 */
	//Comprobar que se ha activado la flag de la interrupcion
	if(TIM3 -> SR & TIM_SR_CC1IF){

		TIM3 -> SR &= ~TIM_SR_CC1IF;

		//Comprobar si el pin echo esta activado o no
		if(HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_4)){
			//Reiniciar el contador
			TIM3->CNT=0;
		}else {

			HAL_TIM_IC_Stop_IT(&htim3, TIM_CHANNEL_1);
			//Guardar el tiempo transcurrido en una variable para pasarla al main
			time_elapsed = HAL_TIM_ReadCapturedValue(&htim3, TIM_CHANNEL_1);
			//Avisar que ha acabado el sistema de medir el tiempo
			time_captured = 1;
		}
	}

  /* USER CODE END TIM3_IRQn 0 */
  /* USER CODE BEGIN TIM3_IRQn 1 */

  /* USER CODE END TIM3_IRQn 1 */
}

